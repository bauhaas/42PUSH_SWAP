#include "libft.h"

void	ft_free_strs(char ***strs)
{
	int	i;

	i = -1;
	while ((*strs)[++i])
	{
		free((*strs)[i]);
		(*strs)[i] = NULL;
	}
	free(*strs);
	*strs = NULL;
}
